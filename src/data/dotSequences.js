const ALPHABET = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
const DIGITS = "0123456789";
const PUNCTUATION = "!?";
const FIREWORKS = "ABC";

const getFrameForChar = (char) => {
  if (ALPHABET.includes(char)) {
    return ALPHABET_FRAMES[ALPHABET.indexOf(char)];
  }
  if (DIGITS.includes(char)) {
    return NUMBER_FRAMES[DIGITS.indexOf(char)];
  }
  if (PUNCTUATION.includes(char)) {
    return PUNCTUATION_FRAMES[PUNCTUATION.indexOf(char)];
  }
  if (char === " ") {
    return ALPHABET_FRAMES[ALPHABET_FRAMES.length - 1];
  }
  return [];
};

const getFramesFromText = (text) => {
  if (!text) {
    return [];
  }
  return text
    .toUpperCase()
    .split("")
    .map((char) => getFrameForChar(char));
};

